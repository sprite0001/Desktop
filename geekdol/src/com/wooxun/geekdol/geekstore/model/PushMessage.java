package com.wooxun.geekdol.geekstore.model;

import com.soft863.dolphin.common.CommonEntity;

public class PushMessage extends CommonEntity{
    
	private static final long serialVersionUID = 1L;

	/** 消息ID */
    private Long id;

    /** 消息内容 */
    private String content;

    /** 操作人 */
    private Long opreator;

    /**
     *
     * 获取  消息ID
     *
     * @return  id
     */
    public Long getId() {
        return id;
    }

    /**
     *
     * 设置  消息ID
     *
     * @param id
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     *
     * 获取  消息内容
     *
     * @return  content
     */
    public String getContent() {
        return content;
    }

    /**
     *
     * 设置  消息内容
     *
     * @param content
     */
    public void setContent(String content) {
        this.content = content == null ? null : content.trim();
    }

    /**
     *
     * 获取  操作人
     *
     * @return  opreator
     */
    public Long getOpreator() {
        return opreator;
    }

    /**
     *
     * 设置  操作人
     *
     * @param opreator
     */
    public void setOpreator(Long opreator) {
        this.opreator = opreator;
    }

}